#include "libft.h"

t_list	*ft_lstnew(void const *content, size_t content_size)
{
	t_list	*res;

	res = (t_list *)ft_memalloc(sizeof(t_list));
	if (res)
	{
		if (content)
		{
			res->content = malloc(content_size);
			if (!res->content)
			{
				free(res);
				return (NULL);
			}
			ft_memcpy(res->content, content, content_size);
			res->content_size = content_size;
		}
	}
	return (res);
}
